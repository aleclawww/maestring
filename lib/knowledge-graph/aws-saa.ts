export function getDomainBySlug(slug: string): DomainDefinition | undefined {
  return DOMAINS.find(d => d.slug === slug)
}

export function getTopicsByDomain(domainSlug: string): TopicDefinition[] {
  return TOPICS.filter(t => t.domainSlug === domainSlug)
}
